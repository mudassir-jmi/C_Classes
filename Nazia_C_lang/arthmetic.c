#include<stdio.h>

// Arthmetic Operator   +, -, *, /, %

void main() {
	int a = 10, b = 20, sum, multiply,sub,divide,mod;
    sum = a + b;
    printf("sum of a +b : %d\n", sum);
    
    sub=a-b;
    printf("sub of a-b : %d\n", sub);
    
    multiply=a*b;
    printf("multiply of a*b : %d\n",multiply);
    
    divide = a/b;
    printf("divide of a/b : %d\n",divide);
    
    mod=a%b;
    printf("mod of a/b : %d\n",mod);
    
    
    
    
}
